/**
 * Get dial code for a country (by English name). Returns e.g. "+966" or "".
 */
export function getDialCodeForCountryName(nameEn) {
  if (!(nameEn || '').trim()) return '';
  const c = countries.find((x) => x.nameEn === nameEn);
  return c ? (countryCodeToDial[c.code] || '') : '';
}

